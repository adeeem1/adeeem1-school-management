/**
 * CRUD functions for Students
 */
export async function dbSaveStudent(student: Student) {
  if (!db) return false;
  try {
    await setDoc(doc(db, 'students', student.id), student);
    return true;
  } catch (error) {
    handleFirestoreError(error, OperationType.WRITE, `students/${student.id}`);
    return false;
  }
}

